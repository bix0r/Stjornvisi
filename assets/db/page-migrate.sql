Stjórnunarverðlaun Stjórnvísi eru veitt árlega þeim stjórnendum sem þykja hafa skarað framúr á sínu sviði. Félagar í Stjórnvísi tilnefna stjórnendur (1 eða fleiri), út frá þeim viðmiðum sem sett eru hverju sinni. Síðan tekur dómnefnd við öllum tilnefningargögnum og vinnur úr þeim. Dómnefnd birtir lista yfir þá sem hljóta lámarksfjölda tilnefninga. Stjórnunarverðlaunin voru veitt í fyrsta sinn í mars 2010.

Forseti Íslands hr. Ólafur Ragnar Grímsson hefur afhent verðlaunin við hátíðlega athöfn í Veisluturninum Kópavogi.

##Markmið
Markmið Stjórnunarverðlauna Stjórnvísi er að vekja athygli á framúrskarandi starfi stjórnenda, sem eru aðilar að Stjórnvísi og hvetja félagsmenn til áframhaldandi faglegra starfa og árangurs.

Það er Stjórnvísi mikið í mun að verðlaunin séu byggð á faglegu mati og hefur því verið vandað verulega til dómnefndar.

